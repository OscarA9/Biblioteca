    private void rellenarCampos(int x){
        Usuario px = LU.ver(x);
        NombreP.setText(px.getNombre());
        ApellidoP.setText(px.getApellido());
        ContraseñaP.setText(px.getContraseña());
        MatriculaP.setText(px.getMatricula());
        TelefonoP.setText(px.getTelefono());
        Cuentas.setSelectedItem(px.getCuenta());
    }
    
     public void bloquearCampos(){
         NombreP.setEditable(false);
        Cuentas.setEnabled(false);
     }

