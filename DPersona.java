    private void AceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AceptarMouseClicked
    if(!opcion){
            LU.agregar(NombreP.getText(),ApellidoP.getText(),MatriculaP.getText(),TelefonoP.getText(), (Cuenta)Cuentas.getSelectedItem(),Encriptar.getMd5(ContraseñaP.getText()));
        }else{
            LU.modificar(edit,NombreP.getText(),ApellidoP.getText(),MatriculaP.getText(),TelefonoP.getText(), (Cuenta)Cuentas.getSelectedItem(),Encriptar.getMd5(ContraseñaP.getText()));
        }
        setVisible(false);
    }//GEN-LAST:event_AceptarMouseClicked

    private void NombrePKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_NombrePKeyTyped
    char caracter = evt.getKeyChar();
        if ((caracter < 'A') || (caracter > 'Z') && (caracter < 'a') || (caracter > 'z')) {
            evt.consume();
            JOptionPane.showMessageDialog(this,"Tecla invalida","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_NombrePKeyTyped

